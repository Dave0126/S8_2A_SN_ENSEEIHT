package pack;

public class Adresse {
	int id;
	String rue;
	String ville;
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId() {
		return this.id;
	}
	
	public void setRue(String rue) {
		this.rue = rue;
	}
	
	public String getRue() {
		return this.rue;
	}
	
	public void setVille(String ville) {
		this.ville = ville;
	}
	
	public String getVille() {
		return this.ville;
	}
}
